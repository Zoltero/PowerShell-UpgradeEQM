<#
    .SYNOPSIS
        Removes database from provided SQL Server instance. 		
    .DESCRIPTION        
        Removes database from provided SQL Server instance.
    .PARAMETER DatabaseServer
        Specifies the SQL Server database server.
    .PARAMETER DatabaseInstance
        Specifies the SQL Server instance.
    .PARAMETER DatabaseName
        Specifies the database name.
#>
function Remove-SqlDatabase
{
    [CmdletBinding()]
    param
        (
            [parameter(Mandatory=$true)]
            [string]$DatabaseServer = "localhost",
            
            [parameter(Mandatory=$false)]            
            [string]$DatabaseInstance = "",
            
            [parameter(Mandatory=$true)]
            [string]$DatabaseName
        )
    PROCESS
    {
        $CurrentLocation = Get-Location

        try
        {
            if($DatabaseInstance -eq "" -or $DatabaseInstance -eq "MSSQLSERVER")
            {
                $DatabaseInstanceUsedBySqlPs = "DEFAULT"
            }
            else
            {
                $DatabaseInstanceUsedBySqlPs = $DatabaseInstance
            }

            if($DatabaseServer -eq '.')
            {
                $DatabaseServer = "localhost"
            }

            $databaseLocation = "SQLSERVER:\SQL\$DatabaseServer\$DatabaseInstanceUsedBySqlPs\Databases\$DatabaseName"

            if(Get-Item $databaseLocation -ErrorAction SilentlyContinue)
            {
                Remove-SqlConnectionsToDatabase -DatabaseServer $DatabaseServer -DatabaseInstance $DatabaseInstance -DatabaseName $DatabaseName
                Remove-Item $databaseLocation
            }
            else
            {
                Write-Error "Database '$DatabaseName' does not exist on SQL Server instance '$DatabaseServer\$DatabaseInstanceUsedBySqlPs'"
            }
        }
        finally
        {
            Set-Location $CurrentLocation
        }
    }
}
Export-ModuleMember -Function Remove-SqlDatabase
