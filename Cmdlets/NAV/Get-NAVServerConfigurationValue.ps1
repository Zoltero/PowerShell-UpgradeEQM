<#
    .SYNOPSIS
        Gets the value for the Microsoft Dynamics NAV Server instance setting in the configuration file.
    .DESCRIPTION        
        Gets the value for the Microsoft Dynamics NAV Server instance setting in the configuration file.
    .PARAMETER ServerInstance
        Specifies the Microsoft Dynamics NAV Server instance
    .PARAMETER ConfigKeyName
        Specifies the setting name in the configuration file of the Microsoft Dynamics NAV Server instance 
#>

function Get-NAVServerConfigurationValue
{
    param
    (
     [parameter(Mandatory=$true)]
     [string]$ServerInstance,
    
     [parameter(Mandatory=$true)]
     [string]$ConfigKeyName
    )
    PROCESS
    {
        $config = Get-NAVServerConfiguration -ServerInstance $ServerInstance -AsXml
        $configSettings = $config.GetElementsByTagName("add");

        $configKeyValue = $configSettings | Where { $_.Attributes["key"].Value -eq $ConfigKeyName } 
        
        if($ConfigKeyValue -eq $null)
        {
            Write-Error "The setting $ConfigKeyName could not be found in the configuration file of Microsoft Dynamics NAV Server $ServerInstance."
            return
        }

        return $configKeyValue.Value        
    }
}

Export-ModuleMember -Function Get-NAVServerConfigurationValue
